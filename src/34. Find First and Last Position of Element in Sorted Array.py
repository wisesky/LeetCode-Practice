from typing import List
class Solution:
    def searchRange(self, nums: List[int], target: int) -> List[int]:
        if len(nums) == 0:
            return [-1,-1]

        st = 0
        ed = len(nums)-1
        
        while st <= ed:
            mid = (st+ed) // 2
            if nums[mid] > target:
                ed = mid-1
            elif nums[mid] < target:
                st = mid+1
            else:
                # leftSearch 在nums[0:mid-1]寻找左边界，如果没有那么左边界就是mid
                # 同上
                leftEdge = self.leftSearch(nums, 0, mid-1, target)
                rightEdge = self.rightSearch(nums, mid+1, len(nums)-1, target)
                return [leftEdge if leftEdge!=-1 else mid, rightEdge if rightEdge!=-1 else mid]
        
        return [-1, -1]

    def leftSearch(self, nums, start, end, target):
        print('LeftSearch', start, end, len(nums))
        if start > len(nums)-1:
            return -1
        while start <= end:
            mid = (start+end) // 2
            if nums[mid] < target:
                start = mid+1
            else: # mid == target
                break

        if start > end:
            return -1
        leftEdge = self.leftSearch(nums, start, mid-1, target)
        return mid if leftEdge == -1 else leftEdge

    def rightSearch(self, nums, start, end, target):
        print('RightSearch', start, end, len(nums))
        if start > len(nums)-1 :
            return -1
        while start <= end:
            mid = (start+end) // 2
            if nums[mid] > target:
                end = mid - 1
            else: # mid == target
                break
        
        if start > end:
            return -1
        rightEdge = self.rightSearch(nums, mid+1, end, target)
        return mid if rightEdge == -1 else rightEdge

